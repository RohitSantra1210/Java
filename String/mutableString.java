package String;

public class mutableString {
    public static void main(String[] args) {
    
        StringBuffer ind = new StringBuffer("Rohit ");
        System.out.println(ind);
        ind.append("Santra");
        System.out.println(ind);

        StringBuilder nd = new StringBuilder("Rohit ");
        System.out.println(nd);
        nd.append("Santra");
        System.out.println(nd);


        String sb = "Virat";
        System.out.println(sb);
        sb = "Kohli";
        System.out.println(sb);










    }
    
}
